#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char			*str;
	unsigned long	i;

	i = 0;
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (s == NULL)
		return (NULL);
	if (str == NULL)
		return (NULL);
	while (len--)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
